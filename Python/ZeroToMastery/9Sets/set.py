# Unordered collection of unique values

my_set = {1,2,235,325,1}
print(my_set)
my_set.add
print(my_set)

# We can cast list to set
my_list = [1,22,3,23,23,23,232,5325,6]
print(my_list)
print(set(my_list))

# set doesn't support indexing (since it uses hashes)
print(235 in my_set)

print(len(my_set))