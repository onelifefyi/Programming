public class StringCompareMethods{
    public static void main(String[] args){
        String vovels = "aeiouAEIOU";
        System.out.println(vovels.contains("A"));
        System.out.println(vovels.contains("z"));
        System.out.println(vovels.indexOf('a'));
        System.out.println(vovels.indexOf('E'));
        System.out.println(vovels.indexOf('Z'));
    }
}